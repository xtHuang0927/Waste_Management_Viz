def guess():

    # Set the initial value of the slide bar
    slider_value = 0

    # Set the range of the slide bar
    slider_range = (1, 50)

    # Generate a random number to be guessed
    target_number = 12
    # Set the initial value of the start button
    start_button_clicked = False
    # Set the initial value of the guess value
    guess_value = None

    # Create the slide bar
    slider = st.slider("Given that you know that the United States has 4% of the world's population, please guess the percentage of the planet's MSW generated by the United States.", *slider_range, value=slider_value, format = "%d%%")

    # Create the start button
    start_button = st.button("Start")

    if start_button:
        start_button_clicked = True
        guess_value = slider

    if start_button_clicked and guess_value == target_number:
        st.success("Congratulations! You guessed the correct number.")
    elif start_button_clicked and guess_value != target_number:
        st.error('Sorry, the truth answer is 12%.')


